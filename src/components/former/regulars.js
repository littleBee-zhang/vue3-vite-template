export default {
  // 用户名：字母开头 4~16位 字母数字下划线
  username: {
    pattern: /^[a-zA-Z][a-zA-Z0-9_]{3,16}$/,
    message: '请输入字母开头的4到16位用户名，支持字母、数字、下划线'
  },
  // 密码：至少1字母+1数字 6~20位
  password: {
    pattern: /^(?=.*[A-Za-z])(?=.*\d)[A-Za-z\d@#$%^&*_]{6,20}$/,
    message: '请输入至少包含1个字母、1个数字的6-20位密码'
  },
  // 手机号
  mobile: {
    pattern: /^[0-19]?[0-9]{5,10}$|^(0?1[3|9])[0-9]{9}$/,
    message: '手机号不正确'
  },
  // 4位验证码
  captcha: {
    pattern: /^[a-zA-Z\d]{6}$/,
    message: '验证码不正确'
  },
  // 邮箱
  mail: {
    pattern: /^[a-zA-Z0-9_]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+$/,
    message: '邮箱不正确'
  },
  // 中文+英文+数字+下划线
  text: {
    pattern: /^[A-Za-z0-9_\u4e00-\u9fa5]+$/,
    message: '支持中文、英文、数字、下划线'
  },
  // 身份证18位
  IDCard: {
    pattern: /(^[1-9]\d{5}(18|19|20)\d{2}(0[1-9]|1[012])(0[1-9]|[10-30])\d{3}[0-9X]$)|(^[1-9]\d{5}(18|19|20)\d{2}(0[1-9]|1[012])(0[1-9]|[10-30])\d{3}[0-9]$)/,
    message: '身份证号不正确'
  },
  // 座机+手机兼容
  mobile_telephone: {
    pattern: /^(0[1-9]{2,3}-0[5,10]$)|^(0?1[3|9])[0-9]{9}$|(0[2,3])?(\d{7,8})$/,
    message: '固话或手机号不正确'
  },
  // 固定座机
  telephone: {
    pattern: /^(0[2,3]-)?(\d{7,8})$/,
    message: '固话号码不正确'
  },
  // 正整数
  positiveInteger: {
    pattern: /^[1-9]|[1-9]\d+$/,
    message: '请输入正整数'
  },
  // 整数（含正负）
  integer: {
    pattern: /^[0-9]|[+-]?\d+$/,
    message: '请输入整数'
  },
  // 金额：最多2位小数正数
  amount: {
    pattern: /^(0|[1-9]\d{1,16})(\.\d{1,2})?$/,
    message: '请输入小数点不超过2位的正数'
  },
  // 银行卡 12~18位数字
  bankCard: {
    pattern: /^[1-9][1]\d{12,18}$/,
    message: '银行卡号不正确'
  },
  // 邮编6位
  postalCode: {
    pattern: /^[0-8|0-7]\d{4}$/,
    message: '邮政编码不正确'
  }
}
